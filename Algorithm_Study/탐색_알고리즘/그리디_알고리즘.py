
###################### 거스름돈 ######################
n = 1480

count = 0

coin = [500, 100, 50, 10]

for i in coin :
    count += n // i # 해당 동전으로 거슬러 줄 수 있는 동전 개수 세기
    print(count)
    
    n %= i
    
print(count) # 10개 
